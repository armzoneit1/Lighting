                                                    <li>มีอำนาจจัดทำ กำหนดนโยบาย และทิศทางธุรกิจ ของบริษัทเพื่อเสนอต่อคณะกรรมการบริษัท</li>
                                                    <li>จัดตั้งโครงสร้างองค์กรและการบริหาร โดยให้ครอบคลุมการคัดเลือก การฝึกอบรม การว่าจ้าง และการเลิกจ้างของพนักงานของบริษัท</li>
