                                                    <li>ปฏิบัติหน้าที่อื่นๆ ตามที่ได้รับมอบหมายจากคณะกรรมการบริษัท การมอบอำนาจดังกล่าวให้แก่คณะกรรมการนั้น กำหนดให้รายการที่กรรมการบริหารหรือบุคคลที่เกี่ยวโยงกัน มีส่วนได้เสีย หรืออาจมีความขัดแย้งทางผลประโยชน์อื่นใด (ตามที่สำนักงาน ก.ล.ต. และตลาดหลักทรัพย์ฯ กาหนด) กับบริษัทหรือบริษัทย่อย ให้กรรมการบริหารซึ่งมีส่วนได้เสียในเรื่องใดไม่มีสิทธิออกเสียงลงคะแนนในเรื่องนั้น</li>
                                                </ul>

                                                <p class="mb-1">2.2 คณะกรรมการตรวจสอบ</p>
                                                <p>คณะกรรมการตรวจสอบ แต่งตั้งโดยคณะกรรมการบริษัท คณะกรรมการตรวจสอบประกอบด้วยกรรมการอิสระอย่างน้อย 3 คน สามารถใช้ดุลยพินิจของตนอย่างเป็นอิสระ สามารถอ่านและเข้าใจพื้นฐานของงบการเงินซึ่งจำเป็นต้องมีในการทำหน้าที่กรรมการตรวจสอบ กรรมการตรวจสอบอย่างน้อย 1 คน ต้องมีความรู้ ความชำนาญ ทางด้านบัญชี หรือการจัดการด้านการเงินที่เกี่ยวข้องตามข้อกำหนดของตลาดหลักทรัพย์ฯคณะกรรมการตรวจสอบต้องจัดทำกฎบัตรของคณะกรรมการตรวจสอบ และเห็นชอบโดยคณะกรรมการบริษัท เพื่อใช้เป็นแนวทางในการปฏิบัติซึ่งครอบคลุมเรื่องดังต่อไปนี้</p>
                                                <ul class="list-decor">
                                                    <li>สอบทานให้บริษัทมีรายงานทางการเงิน (รายไตรมาสและประจำปี) ที่ถูกต้องและเปิดเผยข้อมูลเกี่ยวข้องของบริษัทอย่างเพียงพอก่อนเสนอต่อคณะกรรมการบริษัท</li>
                                                    <li>สอบทานให้บริษัทมีระบบควบคุมภายในและระบบตรวจสอบภายในที่เหมาะสมและมีประสิทธิภาพ</li>
                                                    <li>สอบทานให้บริษัทมีระบบการประเมินความเสี่ยง และการบริหารความเสี่ยงที่เหมาะสมเพียงพอและมีประสิทธิภาพ</li>
                                                    <li>สอบทานให้บริษัทปฏิบัติตามกฎหมายว่าด้วยหลักทรัพย์และตลาดหลักทรัพย์ฯ ข้อกำหนดของตลาดหลักทรัพย์ฯ หรือกฎหมายที่เกี่ยวข้องกับธุรกิจของบริษัท</li>
                                                    <li>พิจารณาคัดเลือกเพื่อเสนอแต่งตั้งและกำหนดค่าตอบแทนผู้สอบบัญชีของบริษัท ประสานงานกับผู้สอบบัญชีเกี่ยวกับวัตถุประสงค์ในการดำเนินการตรวจสอบ ขอบเขต แนวทาง แผนงาน และปัญหาที่พบระหว่างการตรวจสอบ และประเด็นที่ผู้สอบบัญชีเห็นว่าเป็นสาระสำคัญ</li>
                                                    <li>พิจารณาเปิดเผยข้อมูลของบริษัทในกรณีที่เกิดรายการที่เกี่ยวโยงกัน หรือรายการที่อาจมีความขัดแย้งทางผลประโยชน์ให้มีความถูกต้องและครบถ้วน</li>
