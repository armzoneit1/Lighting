                                                    <li>ปฏิบัติหน้าที่อื่นๆ ตามที่ได้รับมอบหมายจากคณะกรรมการบริษัท</li>
                                                </ul>

                                                <p class="mb-1">2.3 คณะกรรมการกำกับดูแลกิจการและสรรหา</p>
